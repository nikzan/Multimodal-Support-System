package com.nova.support.domain.enums;

/**
 * Статусы тикета в системе поддержки
 */
public enum TicketStatus {
    /**
     * Новый тикет, только что создан
     */
    NEW,
    
    /**
     * Тикет обрабатывается AI (транскрипция, анализ)
     */
    PROCESSING,
    
    /**
     * Тикет обработан AI и открыт для ответа оператора
     */
    OPEN,
    
    /**
     * Тикет закрыт (проблема решена)
     */
    CLOSED
}
